int SumDigits(int iNo)
{
    int iSum = 0 , iDigit = 0;

    if(iNo < 0)
    {
        iNo = -iNo ;
    }

    while(iNo != 0)
    {
        iDigit= iNo% 10 ;
        iNo = iNo / 10;
        iSum = iSum + iDigit;
    }
    return iSum;

}// End of CountDigits

///////////////////////////////////////////////////////////////////////////////
//
//Entry point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;
    int iRet = 0;
    

    printf("Enter the number: ");
    scanf("%d",&iValue);

    iRet = SumDigits(iValue);
    printf("Sum of Digits is : %d\n",iRet);

    return 0 ;
}//Enf of main function

