int CountSmall(char ch[])
{
    int iCount = 0;
    while(*ch!= '\0')
    {
        if((*ch >= 'a')&&(*ch <= 'z'))
        {
            iCount++;
        }
        ch++;
    }
    
    return iCount ;

}//end of the CountSmall function

