void PrintEven(int iNo)
{
    int iCnt = 0;
    if(iNo<0)
    {
        iNo = -iNo;
    }
    for(iCnt= 1 ; iCnt<=(2*iNo); iCnt++)
    {
        if((iCnt)%2 == 0)
        {
            printf("%d\t", iCnt);
        }
    }
}//end of the PrintEven function

