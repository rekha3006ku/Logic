void DisplayConvert(char cValue)
{
    if (cValue >= 'A' && cValue <= 'Z')
    {
       cValue = cValue + 32 ;
       printf("%c",cValue);
    }
    
    else if(cValue >= 'a' && cValue <= 'z')
    {
        cValue = cValue - 32;
        printf("%c",cValue);
    }
}//end of the DisplayConvert function

