#include<stdio.h>                               // for input output
#include<stdbool.h>                             // for bool Datatype

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Check
//  Description :   Checks the number id Divisible by 5 or not
//  Input :         Integer
//  output :        Bool
//  Author :        Rekha Shankarlal Kumawat
//  Date :          16/10/2025
//
///////////////////////////////////////////////////////////////////////////////

bool Check(int iNo)
{
    if((iNo % 5)==0)
    {
        return true;
    }
    else
    {
        return false;
    }

}// End of Check function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;
    printf("Enter Number: ");
    scanf("%d",&iValue);

    bRet = Check(iValue);

    if(bRet == true)
    {
        printf("Divisible by 5");
    }
    else
    {
        printf("Not Divisible by 5");
    }
    return 0;

}///////////////////////////////////////////////////////////////////////////////
