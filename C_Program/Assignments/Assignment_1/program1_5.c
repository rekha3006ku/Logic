void Accept(int frequency)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt <= frequency; iCnt++)
    {
        printf("*\t");
    }

}// End of Accept function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("enter the frequency :");
    scanf("%d",&iValue);
    Accept(iValue);
    return 0;
}// end of main function

