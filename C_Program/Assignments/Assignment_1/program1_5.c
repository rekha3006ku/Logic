///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Description :   Display the Character till N freqency
//  Input :         Integer
//  output :        void
//  Author :        Rekha Shankarlal Kumawat
//  Date :          16/10/2025
//
