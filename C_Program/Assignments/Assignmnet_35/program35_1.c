int CountCapital(char ch[])
{
    int iCount = 0;
    while(*ch!= '\0')
    {
        if((*ch >= 'A')&&(*ch <= 'Z'))
        {
            iCount++;
        }
        ch++;
    }
    
    return iCount ;

}//end of the CountCapital function

