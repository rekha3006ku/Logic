void Display(char ch)
{
    if((ch >= 'a')&&(ch <= 'z'))
    {
        ch = ch - 32 ;
        printf("%c \n", ch);
    }
    else if((ch >= 'A')&&(ch <= 'Z'))
    {
        ch = ch + 32 ;
        printf("%c \n", ch);
    }
    else
    {
        printf("%c \n", ch);
    }

}//end of the Display function

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//Entery point function
