void strtoggleX(char ch[])
{
    
    while(*ch!= '\0')
    {
        if((*ch >= 'a')&&(*ch <= 'z'))
        {
            *ch = *ch - 32 ;
        }
        else if((*ch >= 'A')&&(*ch <= 'Z'))
        {
            *ch = *ch + 32 ;
        }
        ch++;
    }

}//end of the strtoggleX function

