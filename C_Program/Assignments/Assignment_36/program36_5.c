int CountWhiteSpace(char ch[])
{
    int iCount = 0 ;
    
    while(*ch!= '\0')
    {
        if(*ch == ' ')
        {
            iCount++;
        }
        ch++;
    }
    return iCount ;

}//end of the CountWhiteSpace function

