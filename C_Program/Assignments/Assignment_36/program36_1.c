void strlwrX(char ch[])
{
    
    while(*ch!= '\0')
    {
        if((*ch >= 'A')&&(*ch <= 'Z'))
        {
            *ch = *ch + 32 ;
        }
        ch++;
    }

}//end of the strlwrX function

