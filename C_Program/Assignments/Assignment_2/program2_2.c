void Display(int iNo)
{
    int iCnt = 0;
    iCnt = 1;

    while(iNo >= iCnt)
    {
        printf("*\t");
        iNo--;
    }
}// End of Display function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue =0 ;
    printf("enter number :");
    scanf("%d",&iValue);

    Display(iValue);
    return 0; 
}// end of main function

