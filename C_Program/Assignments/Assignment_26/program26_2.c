void Display(int iNo)
{
    int iCnt = 0 ;                      
    int iCount = 0;                                 //for printing the numberical value
    
    for(iCnt = iNo ; iCnt >= 1 ; iCnt--)
    {
        if(iCnt%2 == 0)
        {
            printf("#\t");
        }
        else
        {
            iCount= ((iCnt/2)+1);
            printf("%d\t",iCount);
        }                          

    }
}//end of the Display function

